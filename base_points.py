from sapling_jubjub import *

# This is the zero
ZERO = Point(Fq(0), Fq(+1))

# Subgroup A
# Negate the coordinate to get a base point
A0 = Point(Fq(0), Fq(-1))
assert A0 + A0 == ZERO

# Subgroup B
B0 = Point(Fq(+52435875175126190475982595682112313518914282969839895044333406231173219221505), Fq(0))
# It is half A0
assert B0 + B0 == A0

# Negate the coordinate
B1 = Point(Fq(-52435875175126190475982595682112313518914282969839895044333406231173219221505), Fq(0))
# It is another half A0
assert B1 == B0 + A0
assert B1 + B1 == A0

# Subgroup C
C0 = Point(Fq(+51487464086487745867707624970564403863932192230710361188278096157071779040579), Fq(+33175629719884006543435607313513638533300198751199617432860030069151083304669))
# It is half B0
assert C0 + C0 == B0

# All combinations of signs
# - -
C1 = Point(Fq(-51487464086487745867707624970564403863932192230710361188278096157071779040579), Fq(-33175629719884006543435607313513638533300198751199617432860030069151083304669))
assert C1 + C1 == B0
assert C1 + C0 == B1

# + -
C2 = Point(Fq(+51487464086487745867707624970564403863932192230710361188278096157071779040579), Fq(-33175629719884006543435607313513638533300198751199617432860030069151083304669))
assert C2 + C2 == B1
assert C2 + C0 == A0

# - +
C3 = Point(Fq(-51487464086487745867707624970564403863932192230710361188278096157071779040579), Fq(+33175629719884006543435607313513638533300198751199617432860030069151083304669))
assert C3 + C3 == B1
assert C3 + C1 == A0

cogroups = [ZERO, A0, B0, B1, C0, C1, C2, C3]


# A generator of the large subgroup without any component from the cogroups
G = Point(Fq(4139425550610461525665941076812662132363359224232624900223172373014329534291), Fq(39635691377166599497441725607757882405510648532010642268690928210480481875248))
assert G * Fq(r_j) == ZERO

# The opposite point (-u) is in the same subgroup
G_minus_plus  = Point(Fq(-4139425550610461525665941076812662132363359224232624900223172373014329534291), Fq(+39635691377166599497441725607757882405510648532010642268690928210480481875248))
assert G_minus_plus * Fq(r_j) == ZERO
assert G + G_minus_plus == ZERO

# The points with -v are in another subgroup (+A0)
G_plus_minus  = Point(Fq(+4139425550610461525665941076812662132363359224232624900223172373014329534291), Fq(-39635691377166599497441725607757882405510648532010642268690928210480481875248))
assert G_plus_minus * Fq(r_j) == A0
G_minus_minus = Point(Fq(-4139425550610461525665941076812662132363359224232624900223172373014329534291), Fq(-39635691377166599497441725607757882405510648532010642268690928210480481875248))
assert G_minus_minus * Fq(r_j) == A0


